public class Types {

    public static void main(String[] args) {
        // Primitive types (8)

        // 1. Integer types (4)
        byte b = 127; // 1B, 256, min = -128, max = 127
        short s = 32000; // 2B, max ~ 32000
        int x = 1000000000; // 4B, max ~ 2 000 000 000
        long l = 9000000000000000000L; // 8B

        // 2. Floating point types (2)
        float f = 10.5f; // 4B
        double d = 10.5; // 8B

        // 3. Character (1)
        char c = 'a';

        // 4. Logical (1)
        boolean bool = false;

        // Object types
        String str = "Taras";
    }
}
